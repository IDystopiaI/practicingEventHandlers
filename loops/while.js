// while loop = repeat some code while some condition is true

// do while, code executes at least once
let counter = 3;
do {
    counter++;
    console.log(counter);
} while (counter < 1);
console.log("Do while loop finished!");

counter = 10;
while (counter < 10) {
    counter++;
    console.log(counter);
}
console.log("While loop Finished");

counter = 0;
while (counter < 10) {
    counter++;
    console.log(counter);
}
console.log("While Loop Finished");
